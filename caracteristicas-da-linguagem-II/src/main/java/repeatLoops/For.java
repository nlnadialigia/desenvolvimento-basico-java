package repeatLoops;

public class For {
    public static void main(String[] args) {

        for (int i = 0; i <= 10; i = i + 1) {
            System.out.println("i = " + i);
        }

        System.out.println("*");

        for (int x = 0; x <= 5; x++) {
            System.out.println("x = " + x);
        }
    }
}
